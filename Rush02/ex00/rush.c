#include <unistd.h>

void    ft_putstr(char *str)
{
    int i;

    i = 0;
    while (str[i] != '\0')
    {
        write(1, &str[i], 1);
        i++;
    }
}

int ft_check_if_dict(int argc, char **argv)
{
    if (argc < 3)
    {
        return (0);
    }
    return (1);
}
